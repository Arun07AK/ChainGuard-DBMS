-- SECTION 5d: STORED FUNCTION -- fn_validate_chain_integrity
-- ============================================================

CREATE OR REPLACE FUNCTION fn_validate_chain_integrity (
    p_evidence_id IN NUMBER
) RETURN VARCHAR2
AS
    CURSOR c_chain IS
        SELECT Transfer_ID, From_Officer_ID, To_Officer_ID, Transfer_Date
        FROM   Custody_Transfers
        WHERE  Evidence_ID = p_evidence_id
        ORDER  BY Transfer_Date ASC, Transfer_ID ASC;

    v_prev_to NUMBER  := NULL;
    v_prev_dt DATE    := NULL;
    v_first   BOOLEAN := TRUE;
    v_count   NUMBER  := 0;
BEGIN
    FOR r IN c_chain LOOP
        v_count := v_count + 1;
        IF v_first THEN
            IF r.From_Officer_ID <> r.To_Officer_ID THEN
                RETURN 'BROKEN: First record (Transfer_ID=' || r.Transfer_ID || ') is not INITIAL_COLLECTION.';
            END IF;
            v_first := FALSE;
        ELSE
            IF v_prev_to <> r.From_Officer_ID THEN
                RETURN 'BROKEN: Gap at Transfer_ID=' || r.Transfer_ID ||
                       '. Expected From_Officer=' || v_prev_to || ' got ' || r.From_Officer_ID || '.';
            END IF;
            IF (r.Transfer_Date - v_prev_dt) > 30 THEN
                RETURN 'BROKEN: Time gap of ' || ROUND(r.Transfer_Date - v_prev_dt) ||
                       ' days before Transfer_ID=' || r.Transfer_ID || '.';
            END IF;
        END IF;
        v_prev_to := r.To_Officer_ID;
        v_prev_dt := r.Transfer_Date;
    END LOOP;

    IF v_count = 0 THEN RETURN 'BROKEN: No custody records found for Evidence_ID=' || p_evidence_id; END IF;
    RETURN 'INTACT';
END fn_validate_chain_integrity;
/

