-- SECTION 5c: STORED PROCEDURE -- sp_custody_transfer
-- ============================================================

CREATE OR REPLACE PROCEDURE sp_custody_transfer (
    p_evidence_id     IN NUMBER,
    p_from_officer_id IN NUMBER,
    p_to_officer_id   IN NUMBER,
    p_location        IN VARCHAR2,
    p_reason          IN VARCHAR2,
    p_condition       IN VARCHAR2
)
AS
    v_current_custodian NUMBER;
    v_to_auth_level     VARCHAR2(20);
    v_old_transfer_id   NUMBER;
    e_wrong_custodian EXCEPTION;
    e_unauth_officer  EXCEPTION;
BEGIN
    SAVEPOINT sp_transfer_start;

    SELECT Current_Custodian_ID INTO v_current_custodian
    FROM   Evidence WHERE Evidence_ID = p_evidence_id FOR UPDATE;

    IF v_current_custodian <> p_from_officer_id THEN RAISE e_wrong_custodian; END IF;

    SELECT Authorization_Level INTO v_to_auth_level
    FROM   Officers WHERE Officer_ID = p_to_officer_id;

    IF v_to_auth_level = 'OFFICER' AND UPPER(p_reason) LIKE '%LAB%' THEN RAISE e_unauth_officer; END IF;

    SAVEPOINT sp_before_update;

    BEGIN
        SELECT Transfer_ID INTO v_old_transfer_id
        FROM (
            SELECT Transfer_ID FROM Custody_Transfers
            WHERE  Evidence_ID = p_evidence_id AND Status = 'PENDING'
            ORDER  BY Transfer_Date DESC, Transfer_ID DESC
        ) WHERE ROWNUM = 1;

        pkg_bypass.g_in_procedure := TRUE;
        UPDATE Custody_Transfers SET Status='COMPLETED', Is_Sealed='Y' WHERE Transfer_ID = v_old_transfer_id;
        pkg_bypass.g_in_procedure := FALSE;
    EXCEPTION WHEN NO_DATA_FOUND THEN NULL;
    END;

    INSERT INTO Custody_Transfers (
        Evidence_ID, From_Officer_ID, To_Officer_ID,
        Transfer_Date, Location, Reason, Evidence_Condition, Status, Is_Sealed
    ) VALUES (
        p_evidence_id, p_from_officer_id, p_to_officer_id,
        SYSDATE, p_location, p_reason, p_condition, 'PENDING', 'N'
    );

    UPDATE Evidence
    SET Current_Custodian_ID = p_to_officer_id, Current_Location = p_location, Condition = p_condition
    WHERE Evidence_ID = p_evidence_id;

    COMMIT;
    pkg_bypass.g_in_procedure := FALSE;
    DBMS_OUTPUT.PUT_LINE('SUCCESS: Evidence ' || p_evidence_id || ' transferred to Officer ' || p_to_officer_id);

EXCEPTION
    WHEN e_wrong_custodian THEN
        ROLLBACK TO sp_transfer_start; pkg_bypass.g_in_procedure := FALSE;
        RAISE_APPLICATION_ERROR(-20002, 'Transfer failed: Officer ' || p_from_officer_id || ' is not the current custodian.');
    WHEN e_unauth_officer THEN
        ROLLBACK TO sp_transfer_start; pkg_bypass.g_in_procedure := FALSE;
        RAISE_APPLICATION_ERROR(-20003, 'Transfer failed: Officer ' || p_to_officer_id || ' lacks authorization.');
    WHEN OTHERS THEN
        ROLLBACK TO sp_transfer_start; pkg_bypass.g_in_procedure := FALSE; RAISE;
END sp_custody_transfer;
/

