-- Query 1: Complete custody chain for Evidence ID 1 (3-table join)
SELECT
    e.Evidence_ID, e.Description AS Evidence_Description, e.Tamper_Seal,
    ct.Transfer_ID, ct.Transfer_Date,
    f.Name AS From_Officer, f.Badge_Number AS From_Badge,
    t.Name AS To_Officer,   t.Badge_Number AS To_Badge,
    ct.Location, ct.Reason, ct.Status AS Transfer_Status, ct.Is_Sealed
FROM Evidence e
JOIN Custody_Transfers ct ON e.Evidence_ID     = ct.Evidence_ID
JOIN Officers f           ON ct.From_Officer_ID = f.Officer_ID
JOIN Officers t           ON ct.To_Officer_ID   = t.Officer_ID
WHERE e.Evidence_ID = 1
ORDER BY ct.Transfer_Date ASC;

-- Query 2: All evidence for Case 1 with current custodian details (3-table join)
SELECT
    c.FIR_Number, c.Crime_Type,
    e.Evidence_ID, e.Description, e.Category, e.Condition, e.Tamper_Seal,
    o.Name AS Current_Custodian, o.Badge_Number, o.Department, e.Current_Location
FROM Cases c
JOIN Evidence e ON c.Case_ID              = e.Case_ID
JOIN Officers o ON e.Current_Custodian_ID = o.Officer_ID
WHERE c.Case_ID = 1
ORDER BY e.Evidence_ID;

-- Query 3: Evidence that has never been transferred out (correlated subquery)
SELECT
    e.Evidence_ID, e.Description, e.Category, e.Date_Collected, e.Tamper_Seal,
    o.Name AS Initial_Custodian
FROM Evidence e
JOIN Officers o ON e.Current_Custodian_ID = o.Officer_ID
WHERE NOT EXISTS (
    SELECT 1 FROM Custody_Transfers ct
    WHERE ct.Evidence_ID = e.Evidence_ID
      AND ct.Reason <> 'INITIAL_COLLECTION'
);

-- Query 4: Officers who have handled evidence from more than 2 different cases (correlated subquery)
SELECT
    o.Officer_ID, o.Name, o.Badge_Number, o.Rank, o.Department,
    (SELECT COUNT(DISTINCT e2.Case_ID)
     FROM Custody_Transfers ct2
     JOIN Evidence e2 ON ct2.Evidence_ID = e2.Evidence_ID
     WHERE ct2.From_Officer_ID = o.Officer_ID
        OR ct2.To_Officer_ID   = o.Officer_ID) AS Cases_Handled
FROM Officers o
WHERE (
    SELECT COUNT(DISTINCT e.Case_ID)
    FROM Custody_Transfers ct
    JOIN Evidence e ON ct.Evidence_ID = e.Evidence_ID
    WHERE ct.From_Officer_ID = o.Officer_ID
       OR ct.To_Officer_ID   = o.Officer_ID
) > 2
ORDER BY Cases_Handled DESC;

-- Query 5: Evidence count per case + average lab analysis duration (GROUP BY + aggregate)
SELECT
    c.Case_ID, c.FIR_Number, c.Crime_Type, c.Status,
    COUNT(DISTINCT e.Evidence_ID)              AS Evidence_Count,
    ROUND(AVG(la.End_Date - la.Start_Date), 1) AS Avg_Analysis_Days,
    COUNT(DISTINCT la.Analysis_ID)             AS Total_Analyses
FROM Cases c
LEFT JOIN Evidence e     ON c.Case_ID     = e.Case_ID
LEFT JOIN Lab_Analysis la ON e.Evidence_ID = la.Evidence_ID AND la.Status = 'COMPLETED'
GROUP BY c.Case_ID, c.FIR_Number, c.Crime_Type, c.Status
ORDER BY Evidence_Count DESC;

-- Query 6: Crime types with more than 2 evidence items (GROUP BY + HAVING)
SELECT
    c.Crime_Type,
    COUNT(e.Evidence_ID)      AS Total_Evidence,
    COUNT(DISTINCT c.Case_ID) AS Num_Cases
FROM Cases c
JOIN Evidence e ON c.Case_ID = e.Case_ID
GROUP BY c.Crime_Type
HAVING COUNT(e.Evidence_ID) > 2
ORDER BY Total_Evidence DESC;

-- Query 7: Departments ranked by transfer activity (aggregate)
SELECT
    o.Department,
    COUNT(DISTINCT o.Officer_ID)   AS Officers_In_Dept,
    COUNT(ct.Transfer_ID)          AS Total_Transfers_Handled,
    COUNT(DISTINCT ct.Evidence_ID) AS Distinct_Evidence_Handled
FROM Officers o
LEFT JOIN (
    SELECT From_Officer_ID AS Officer_ID, Transfer_ID, Evidence_ID FROM Custody_Transfers
    UNION ALL
    SELECT To_Officer_ID,                 Transfer_ID, Evidence_ID FROM Custody_Transfers
) ct ON o.Officer_ID = ct.Officer_ID
GROUP BY o.Department
ORDER BY Total_Transfers_Handled DESC;

-- Query 8: Evidence overdue in transit — PENDING transfers older than 2 days
SELECT
    e.Evidence_ID, e.Description, e.Tamper_Seal,
    c.FIR_Number, c.Crime_Type,
    ct.Transfer_ID, ct.Transfer_Date,
    ROUND(SYSDATE - ct.Transfer_Date) AS Days_Overdue,
    f.Name AS From_Officer, t.Name AS Assigned_To, t.Phone AS Assignee_Phone,
    ct.Location AS Last_Known_Location
FROM Custody_Transfers ct
JOIN Evidence e  ON ct.Evidence_ID     = e.Evidence_ID
JOIN Cases c     ON e.Case_ID          = c.Case_ID
JOIN Officers f  ON ct.From_Officer_ID = f.Officer_ID
JOIN Officers t  ON ct.To_Officer_ID   = t.Officer_ID
WHERE ct.Status = 'PENDING'
  AND ct.Transfer_Date < SYSDATE - 2
ORDER BY Days_Overdue DESC;

