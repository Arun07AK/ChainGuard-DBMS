-- 1. Officers
CREATE TABLE Officers (
    Officer_ID          NUMBER(6)       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name                VARCHAR2(100)   NOT NULL,
    Badge_Number        VARCHAR2(20)    NOT NULL UNIQUE,
    Rank                VARCHAR2(50)    NOT NULL,
    Department          VARCHAR2(100)   NOT NULL,
    Authorization_Level VARCHAR2(20)    NOT NULL
        CHECK (Authorization_Level IN ('OFFICER','ANALYST','SUPERVISOR','ADMIN')),
    Phone               VARCHAR2(15),
    Email               VARCHAR2(100)
);

-- 2. Forensic_Labs
CREATE TABLE Forensic_Labs (
    Lab_ID          NUMBER(6)       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Lab_Name        VARCHAR2(100)   NOT NULL,
    Location        VARCHAR2(150)   NOT NULL,
    Specialization  VARCHAR2(100),
    Contact_Phone   VARCHAR2(15)
);

-- 3. Storage_Facilities
CREATE TABLE Storage_Facilities (
    Facility_ID     NUMBER(6)       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Facility_Name   VARCHAR2(100)   NOT NULL,
    Location        VARCHAR2(150)   NOT NULL,
    Capacity        NUMBER(6)       NOT NULL CHECK (Capacity > 0),
    Facility_Type   VARCHAR2(20)    NOT NULL
        CHECK (Facility_Type IN ('COLD_STORAGE','SECURE_VAULT','EVIDENCE_ROOM','DIGITAL_VAULT'))
);

-- 4. Cases (FK -> Officers)
CREATE TABLE Cases (
    Case_ID                  NUMBER(6)    GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FIR_Number               VARCHAR2(30) NOT NULL UNIQUE,
    Crime_Type               VARCHAR2(100) NOT NULL,
    Date_Reported            DATE          NOT NULL,
    Status                   VARCHAR2(25)  NOT NULL DEFAULT 'OPEN'
        CHECK (Status IN ('OPEN','CLOSED','UNDER_INVESTIGATION','ARCHIVED')),
    Investigating_Officer_ID NUMBER(6)     NOT NULL,
    Description              VARCHAR2(500),
    CONSTRAINT fk_case_officer FOREIGN KEY (Investigating_Officer_ID)
        REFERENCES Officers(Officer_ID)
);

-- 5. Evidence (FK -> Cases, Officers, Storage_Facilities)
CREATE TABLE Evidence (
    Evidence_ID          NUMBER(6)    GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Case_ID              NUMBER(6)    NOT NULL,
    Description          VARCHAR2(300) NOT NULL,
    Category             VARCHAR2(20)  NOT NULL
        CHECK (Category IN ('WEAPON','BIOLOGICAL','ELECTRONIC','DOCUMENT','NARCOTIC','OTHER')),
    Condition            VARCHAR2(20)  NOT NULL DEFAULT 'INTACT'
        CHECK (Condition IN ('INTACT','DAMAGED','DEGRADED','CONTAMINATED')),
    Date_Collected       DATE          NOT NULL,
    Current_Location     VARCHAR2(150) NOT NULL,
    Current_Custodian_ID NUMBER(6)     NOT NULL,
    Facility_ID          NUMBER(6),
    Tamper_Seal          VARCHAR2(50)  NOT NULL UNIQUE,
    CONSTRAINT fk_ev_case     FOREIGN KEY (Case_ID)              REFERENCES Cases(Case_ID),
    CONSTRAINT fk_ev_officer  FOREIGN KEY (Current_Custodian_ID) REFERENCES Officers(Officer_ID),
    CONSTRAINT fk_ev_facility FOREIGN KEY (Facility_ID)          REFERENCES Storage_Facilities(Facility_ID)
);

-- 6. Custody_Transfers (FK -> Evidence, Officers x2)
CREATE TABLE Custody_Transfers (
    Transfer_ID        NUMBER(8)    GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Evidence_ID        NUMBER(6)    NOT NULL,
    From_Officer_ID    NUMBER(6)    NOT NULL,
    To_Officer_ID      NUMBER(6)    NOT NULL,
    Transfer_Date      DATE         NOT NULL DEFAULT SYSDATE,
    Location           VARCHAR2(150) NOT NULL,
    Reason             VARCHAR2(200) NOT NULL,
    Evidence_Condition VARCHAR2(20)  NOT NULL
        CHECK (Evidence_Condition IN ('INTACT','DAMAGED','DEGRADED','CONTAMINATED')),
    Status             VARCHAR2(15)  NOT NULL DEFAULT 'PENDING'
        CHECK (Status IN ('PENDING','COMPLETED','CANCELLED')),
    Is_Sealed          CHAR(1)       NOT NULL DEFAULT 'N'
        CHECK (Is_Sealed IN ('Y','N')),
    CONSTRAINT fk_ct_evidence FOREIGN KEY (Evidence_ID)     REFERENCES Evidence(Evidence_ID),
    CONSTRAINT fk_ct_from_off FOREIGN KEY (From_Officer_ID) REFERENCES Officers(Officer_ID),
    CONSTRAINT fk_ct_to_off   FOREIGN KEY (To_Officer_ID)   REFERENCES Officers(Officer_ID)
);

-- 7. Lab_Analysis
CREATE TABLE Lab_Analysis (
    Analysis_ID        NUMBER(6)    GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Evidence_ID        NUMBER(6)    NOT NULL,
    Lab_ID             NUMBER(6)    NOT NULL,
    Analyst_Officer_ID NUMBER(6)    NOT NULL,
    Start_Date         DATE         NOT NULL,
    End_Date           DATE,
    Findings           CLOB,
    Status             VARCHAR2(15) NOT NULL DEFAULT 'PENDING'
        CHECK (Status IN ('PENDING','IN_PROGRESS','COMPLETED')),
    CONSTRAINT fk_la_evidence FOREIGN KEY (Evidence_ID)        REFERENCES Evidence(Evidence_ID),
    CONSTRAINT fk_la_lab      FOREIGN KEY (Lab_ID)             REFERENCES Forensic_Labs(Lab_ID),
    CONSTRAINT fk_la_analyst  FOREIGN KEY (Analyst_Officer_ID) REFERENCES Officers(Officer_ID),
    CONSTRAINT chk_la_dates   CHECK (End_Date IS NULL OR End_Date >= Start_Date)
);

-- 8. Court_Requisitions
CREATE TABLE Court_Requisitions (
    Requisition_ID  NUMBER(6)    GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Evidence_ID     NUMBER(6)    NOT NULL,
    Case_ID         NUMBER(6)    NOT NULL,
    Court_Name      VARCHAR2(150) NOT NULL,
    Hearing_Date    DATE          NOT NULL,
    Requested_By_ID NUMBER(6)    NOT NULL,
    Status          VARCHAR2(15) NOT NULL DEFAULT 'PENDING'
        CHECK (Status IN ('PENDING','APPROVED','RETURNED','REJECTED')),
    Request_Date    DATE         NOT NULL DEFAULT SYSDATE,
    CONSTRAINT fk_cr_evidence FOREIGN KEY (Evidence_ID)     REFERENCES Evidence(Evidence_ID),
    CONSTRAINT fk_cr_case     FOREIGN KEY (Case_ID)         REFERENCES Cases(Case_ID),
    CONSTRAINT fk_cr_officer  FOREIGN KEY (Requested_By_ID) REFERENCES Officers(Officer_ID)
);

-- 9. Audit_Log
CREATE TABLE Audit_Log (
    Log_ID           NUMBER(10)   GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Table_Name       VARCHAR2(50) NOT NULL,
    Operation        VARCHAR2(10) NOT NULL
        CHECK (Operation IN ('INSERT','UPDATE','DELETE')),
    Record_ID        NUMBER(10)   NOT NULL,
    Officer_ID       NUMBER(6),
    Action_Timestamp TIMESTAMP    NOT NULL DEFAULT SYSTIMESTAMP,
    Old_Value        CLOB,
    New_Value        CLOB,
    IP_Note          VARCHAR2(200),
    CONSTRAINT fk_al_officer FOREIGN KEY (Officer_ID) REFERENCES Officers(Officer_ID)
);

-- Indexes
CREATE INDEX idx_ev_case     ON Evidence(Case_ID);
CREATE INDEX idx_ct_evidence ON Custody_Transfers(Evidence_ID);
CREATE INDEX idx_ct_date     ON Custody_Transfers(Transfer_Date);

