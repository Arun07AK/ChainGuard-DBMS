CREATE OR REPLACE PACKAGE BODY pkg_chainguard_reports AS

    PROCEDURE generate_case_report (p_case_id IN NUMBER) AS
        CURSOR c_ev IS
            SELECT Evidence_ID, Description, Category, Tamper_Seal, Condition
            FROM Evidence WHERE Case_ID = p_case_id ORDER BY Evidence_ID;
        CURSOR c_tr (p_ev NUMBER) IS
            SELECT ct.Transfer_Date, f.Name AS From_Off, t.Name AS To_Off,
                   ct.Location, ct.Reason, ct.Status, ct.Is_Sealed
            FROM Custody_Transfers ct
            JOIN Officers f ON ct.From_Officer_ID = f.Officer_ID
            JOIN Officers t ON ct.To_Officer_ID   = t.Officer_ID
            WHERE ct.Evidence_ID = p_ev
            ORDER BY ct.Transfer_Date ASC, ct.Transfer_ID ASC;
        v_fir VARCHAR2(30);
    BEGIN
        SELECT FIR_Number INTO v_fir FROM Cases WHERE Case_ID = p_case_id;
        DBMS_OUTPUT.PUT_LINE('================================================');
        DBMS_OUTPUT.PUT_LINE('CHAINGUARD CUSTODY CHAIN REPORT');
        DBMS_OUTPUT.PUT_LINE('Case: ' || p_case_id || ' | FIR: ' || v_fir);
        DBMS_OUTPUT.PUT_LINE('Generated: ' || TO_CHAR(SYSDATE,'DD-MON-YYYY HH24:MI:SS'));
        DBMS_OUTPUT.PUT_LINE('================================================');
        FOR ev IN c_ev LOOP
            DBMS_OUTPUT.PUT_LINE('');
            DBMS_OUTPUT.PUT_LINE('EVIDENCE #' || ev.Evidence_ID || ' [' || ev.Category || '] ' || ev.Description);
            DBMS_OUTPUT.PUT_LINE('  Seal  : ' || ev.Tamper_Seal);
            DBMS_OUTPUT.PUT_LINE('  Chain : ' || fn_validate_chain_integrity(ev.Evidence_ID));
            FOR tr IN c_tr(ev.Evidence_ID) LOOP
                DBMS_OUTPUT.PUT_LINE('    ' || TO_CHAR(tr.Transfer_Date,'DD-MON-YYYY') ||
                    ' | ' || tr.From_Off || ' -> ' || tr.To_Off ||
                    ' | ' || tr.Reason || ' | ' || tr.Status ||
                    CASE WHEN tr.Is_Sealed='Y' THEN ' [SEALED]' ELSE '' END);
            END LOOP;
        END LOOP;
        DBMS_OUTPUT.PUT_LINE('');
        DBMS_OUTPUT.PUT_LINE('=== END OF REPORT ===');
    EXCEPTION WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('ERROR: Case ID ' || p_case_id || ' not found.');
    END generate_case_report;

    PROCEDURE flag_overdue_evidence AS
        CURSOR c_ov IS
            SELECT e.Evidence_ID, e.Description, e.Tamper_Seal,
                   c.FIR_Number, c.Crime_Type,
                   ct.Transfer_ID, ct.Transfer_Date,
                   ROUND(SYSDATE - ct.Transfer_Date) AS Days_Overdue,
                   f.Name AS From_Officer, t.Name AS Assigned_To, t.Phone
            FROM Custody_Transfers ct
            JOIN Evidence e ON ct.Evidence_ID     = e.Evidence_ID
            JOIN Cases c    ON e.Case_ID          = c.Case_ID
            JOIN Officers f ON ct.From_Officer_ID = f.Officer_ID
            JOIN Officers t ON ct.To_Officer_ID   = t.Officer_ID
            WHERE ct.Status = 'PENDING' AND ct.Transfer_Date < SYSDATE - 2
            ORDER BY Days_Overdue DESC;
        v_n NUMBER := 0;
    BEGIN
        DBMS_OUTPUT.PUT_LINE('================================================');
        DBMS_OUTPUT.PUT_LINE('CHAINGUARD OVERDUE EVIDENCE ALERT');
        DBMS_OUTPUT.PUT_LINE('Run: ' || TO_CHAR(SYSDATE,'DD-MON-YYYY HH24:MI:SS'));
        DBMS_OUTPUT.PUT_LINE('================================================');
        FOR r IN c_ov LOOP
            v_n := v_n + 1;
            DBMS_OUTPUT.PUT_LINE('ALERT #' || v_n || ': ' || r.Evidence_ID || ' - ' || r.Description);
            DBMS_OUTPUT.PUT_LINE('  FIR     : ' || r.FIR_Number || ' (' || r.Crime_Type || ')');
            DBMS_OUTPUT.PUT_LINE('  Overdue : ' || r.Days_Overdue || ' day(s) since ' || TO_CHAR(r.Transfer_Date,'DD-MON-YYYY'));
            DBMS_OUTPUT.PUT_LINE('  Assigned: ' || r.Assigned_To || ' | ' || r.Phone);
        END LOOP;
        IF v_n = 0 THEN DBMS_OUTPUT.PUT_LINE('No overdue transfers found.');
        ELSE DBMS_OUTPUT.PUT_LINE('TOTAL OVERDUE: ' || v_n); END IF;
        DBMS_OUTPUT.PUT_LINE('================================================');
    END flag_overdue_evidence;

END pkg_chainguard_reports;
